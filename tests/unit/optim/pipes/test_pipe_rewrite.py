@functools.cache
def _build_self_matching_model(
    pattern_name: str,
    max_iter: int = 50,
) -> tuple[onnx.ModelProto, Any, Any]:
    """Build a minimal ONNX model that self-matches the named pattern."""
    gen_class = get_pattern_input_generator(pattern_name)
    gen = gen_class(domain_versions=TEST_DOMAIN_VERSIONS)
    pattern = gen.pattern

    for i, (kwargs, tags) in enumerate(gen.iter()):
        if i >= max_iter:
            break
        output_dtypes = gen.infer_output_types(kwargs, tags)
        if not output_dtypes:
            continue
        for is_constant_map in gen._iter_constant_combinations(kwargs):
            try:
                model = gen._create_model(kwargs, is_constant_map, output_dtypes)
                matcher = PatternMatcher(model)
                matcher.register_pattern(pattern)
                results = matcher.match()
            except (
                onnx.checker.ValidationError,
                InvalidPatternMatcherModelError,
                ValueError,
                AssertionError,
            ):
                continue
            if len(results) == 1:
                return model, pattern, results[0]

    raise ValueError(
        f"Could not build a self-matching model for '{pattern_name}'. "
        "This indicates a bug in the pattern or its input generator."
    )


def _make_mock_match(node_names: list[str]) -> PatternMatchResult:
    """Create a minimal PatternMatchResult with nodes of given names."""
    nodes = [oh.make_node("Relu", ["x"], ["y"], name=name) for name in node_names]

    class _MockPattern:
        pass

    skeleton = SkeletonMatchResult(
        pattern=_MockPattern(),  # type: ignore[arg-type]
        matched_nodes=nodes,
        matched_node_keys=[n.name if n.name else f"{n.op_type}_node" for n in nodes],
        matcher=None,  # type: ignore[arg-type]
    )
    return PatternMatchResult(
        skeleton_match_result=skeleton,
        schema_input_to_value={},
        schema_output_to_value={},
        type_param_to_type={},
    )


