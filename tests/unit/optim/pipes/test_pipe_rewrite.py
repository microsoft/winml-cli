class TestRewriteGroupDiscovery:
    """Verify JSON-based autodiscovery of rewrite groups."""

    def test_rewrite_groups_not_empty(self) -> None:
        assert len(REWRITE_GROUPS) > 0

    def test_each_group_has_at_least_one_source(self) -> None:
        for group in REWRITE_GROUPS:
            assert len(group.sources) >= 1, f"Group {group.pattern_id!r} has no sources"

    def test_each_group_has_a_target(self) -> None:
        for group in REWRITE_GROUPS:
            assert group.target_class, f"Group {group.pattern_id!r} has empty target"

    def test_flag_names_are_kebab_case(self) -> None:
        import re

        for group in REWRITE_GROUPS:
            name = group.flag_name
            assert re.fullmatch(r"[a-zA-Z0-9\-]+", name), f"Bad flag name: {name!r}"

    def test_rewrite_capabilities_matches_groups(self) -> None:
        # Each group registers a group-level flag, plus per-source flags for
        # multi-source groups → total caps >= number of groups.
        assert len(REWRITE_CAPABILITIES) >= len(REWRITE_GROUPS)
        # Every group flag must appear in REWRITE_CAPABILITIES.
        for group in REWRITE_GROUPS:
            assert group.flag_name in REWRITE_CAPABILITIES

    def test_gelu_group_exists(self) -> None:
        assert any("gelu" in g.pattern_id.lower() for g in REWRITE_GROUPS)

    def test_gemm_group_exists(self) -> None:
        assert any("gemm" in g.pattern_id.lower() for g in REWRITE_GROUPS)


