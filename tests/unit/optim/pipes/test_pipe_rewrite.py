class TestGetRewriteRulesForCapability:
    """Verify get_rewrite_rules_for_capability() returns correct rules."""

    def test_valid_cap_returns_rule_list(self) -> None:
        cap_name = next(iter(REWRITE_CAPABILITIES))
        rules = get_rewrite_rules_for_capability(cap_name)
        assert isinstance(rules, list)
        assert len(rules) >= 1
        assert all(isinstance(r, RewriteRule) for r in rules)

    def test_grouped_cap_returns_multiple_rules(self) -> None:
        multi = next((g for g in REWRITE_GROUPS if len(g.sources) > 1), None)
        if multi is None:
            pytest.skip("No multi-source group found")
        rules = get_rewrite_rules_for_capability(multi.flag_name)
        assert len(rules) == len(multi.sources)

    def test_single_source_cap_returns_one_rule(self) -> None:
        single = next((g for g in REWRITE_GROUPS if len(g.sources) == 1), None)
        if single is None:
            pytest.skip("No single-source group found")
        rules = get_rewrite_rules_for_capability(single.flag_name)
        assert len(rules) == 1

    def test_unknown_cap_raises_value_error(self) -> None:
        with pytest.raises(ValueError, match="Unknown rewrite capability"):
            get_rewrite_rules_for_capability("nosuchpattern-alsononexistent")


