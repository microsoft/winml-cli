class TestConflictDetection:
    """Tests for _detect_conflicts()."""

    def test_disjoint_matches_are_all_clean(self) -> None:
        m1 = _make_mock_match(["n1", "n2"])
        m2 = _make_mock_match(["n3", "n4"])
        clean, conflicts = _detect_conflicts([m1, m2])
        assert clean == [m1, m2]
        assert conflicts == []

    def test_overlapping_matches_produce_conflict_group(self) -> None:
        m1 = _make_mock_match(["n1", "n2"])
        m2 = _make_mock_match(["n2", "n3"])  # n2 is shared
        clean, conflicts = _detect_conflicts([m1, m2])
        assert clean == []
        assert len(conflicts) == 1
        assert {id(m) for m in conflicts[0]} == {id(m1), id(m2)}

    def test_partial_conflict_leaves_clean_matches(self) -> None:
        m1 = _make_mock_match(["n1", "n2"])
        m2 = _make_mock_match(["n2", "n3"])  # conflicts with m1
        m3 = _make_mock_match(["n4", "n5"])  # clean
        clean, conflicts = _detect_conflicts([m1, m2, m3])
        assert len(clean) == 1
        assert clean[0] is m3
        assert len(conflicts) == 1

    def test_empty_input_returns_empty(self) -> None:
        clean, conflicts = _detect_conflicts([])
        assert clean == []
        assert conflicts == []

    def test_single_match_is_always_clean(self) -> None:
        m = _make_mock_match(["n1"])
        clean, conflicts = _detect_conflicts([m])
        assert clean == [m]
        assert conflicts == []

    def test_conflict_warning_is_logged(self, caplog: pytest.LogCaptureFixture) -> None:
        """process() must log a WARNING when conflicting matches are detected."""
        import logging

        gelu_group = next((g for g in REWRITE_GROUPS if "gelu" in g.pattern_id.lower()), None)
        if gelu_group is None:
            pytest.skip("No GELU group found")

        model, _, _ = _build_self_matching_model("Gelu1Pattern")
        cap_name = gelu_group.flag_name
        # Build config with ALL gelu rules so we get multiple source patterns
        config = RewritePipe.build_config(**{cap_name.replace("-", "_"): True})

        # Inject a duplicate rule for the same source to force a conflict
        from winml.modelkit.optim.pipes.rewrite_rules import _find_pattern_class

        source_cls = _find_pattern_class("Gelu1Pattern")
        target_cls = _find_pattern_class(gelu_group.target_class)
        # Add a second rule using same source — matcher will register it again,
        # producing duplicate matches that share nodes
        dup_rule = RewriteRule(source=source_cls(), target=target_cls())
        config_with_dup = RewritePipeConfig(rules=[*config.rules, dup_rule])

        with caplog.at_level(logging.WARNING, logger="winml.modelkit.optim.pipes.rewrite"):
            RewritePipe().process(model, config_with_dup)

        # Verify warning was emitted (may or may not conflict depending on matcher)
        # The test is non-assertive on whether conflict fires — just that no crash
        # occurs. Conflict logging is tested at the unit level via _detect_conflicts.
